    // Get Data from `products`
    private void getData(){
        try {
            
            Connect Connect = new Connect();
            Connection con = Connect.conn();
            
            Statement st = con.createStatement();
            String sql = "SELECT transactions.datecreated, transactions.id, SUM(sold.quantity) FROM "
                    + "`transactions` INNER JOIN `sold` ON transactions.id = tr_id GROUP BY transactions.id    ";
            ResultSet rs = st.executeQuery(sql);
            
            while(rs.next()){
                String id = String.valueOf(rs.getInt("id"));
                String sum = rs.getString("sum(sold.quantity)");
                String date = rs.getString("datecreated");
                
                String tableData[] = {
                    id,
                    id,
                    date,
                    sum
                };
                DefaultTableModel tblModel = (DefaultTableModel)jTable1.getModel();
                
                tblModel.addRow(tableData);
            }
            con.close();
        }
        catch(Exception e){
            System.out.println(e.getMessage());
        }
    }
    
